//! TCP Fast Open (TFO) extension for the ONCRIX network stack.
//!
//! Implements RFC 7413 TCP Fast Open, which allows data exchange
//! during the initial TCP handshake by using cryptographic cookies.
//! On a repeated connection to the same server, the client can send
//! application data in the SYN packet, reducing round-trip latency
//! from two RTTs (connect + first write) to one.
//!
//! # Cookie mechanism
//!
//! 1. The client sends a SYN with an empty TFO cookie option,
//!    requesting a cookie from the server.
//! 2. The server generates an 8-byte [`TfoCookie`] based on the
//!    client's IP address and a secret key, returning it in the
//!    SYN+ACK.
//! 3. On subsequent connections, the client includes the cookie
//!    (and optional data) in the SYN.
//! 4. The server validates the cookie and, if valid, immediately
//!    delivers the SYN data to the application.
//!
//! # Key structures
//!
//! | Type              | Purpose                                       |
//! |-------------------|-----------------------------------------------|
//! | [`TfoState`]      | Per-connection TFO negotiation state          |
//! | [`TfoCookie`]     | 8-byte opaque cookie value                    |
//! | [`TfoCookieJar`]  | Server-side cookie cache (64 entries)         |
//! | [`TfoConnection`] | TCP connection with TFO extensions            |
//! | [`TfoStats`]      | Global TFO statistics counters                |
//! | [`TfoRegistry`]   | Registry of TFO-enabled listening sockets     |

use oncrix_lib::{Error, Result};

// =========================================================================
// Constants
// =========================================================================

/// Size of a TFO cookie in bytes.
const TFO_COOKIE_LEN: usize = 8;

/// Maximum number of cached cookies in a [`TfoCookieJar`].
const TFO_COOKIE_JAR_SIZE: usize = 64;

/// Cookie expiry in ticks (approximately 5 minutes at 1 tick/s).
const TFO_COOKIE_EXPIRY_TICKS: u64 = 300;

/// Maximum data that may be carried in a SYN packet (bytes).
const TFO_MAX_SYN_DATA: usize = 1460;

/// Maximum number of TFO-enabled listening sockets.
const TFO_REGISTRY_SIZE: usize = 32;

/// Maximum number of bytes in the send buffer.
const SEND_BUF_SIZE: usize = 4096;

/// Maximum number of bytes in the receive buffer.
const RECV_BUF_SIZE: usize = 4096;

// =========================================================================
// TfoState
// =========================================================================

/// Per-connection TFO negotiation state.
///
/// Tracks the progression of the TCP Fast Open cookie exchange
/// between client and server during connection setup.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum TfoState {
    /// TFO is disabled for this connection.
    Disabled,
    /// Client is requesting a cookie from the server (SYN with
    /// empty cookie option).
    Requesting,
    /// Server has accepted the TFO cookie; SYN data is being
    /// delivered to the application.
    Accepted,
    /// TFO handshake completed; connection is fully established
    /// with data already exchanged.
    Established,
}

// =========================================================================
// TfoCookie
// =========================================================================

/// An 8-byte TCP Fast Open cookie.
///
/// Generated by the server from the client's IP address and a
/// server-side secret key.  The cookie is opaque to the client.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct TfoCookie {
    /// Raw cookie bytes.
    bytes: [u8; TFO_COOKIE_LEN],
}

impl TfoCookie {
    /// Create a new cookie with the given bytes.
    pub const fn new(bytes: [u8; TFO_COOKIE_LEN]) -> Self {
        Self { bytes }
    }

    /// Return the raw cookie bytes.
    pub const fn as_bytes(&self) -> &[u8; TFO_COOKIE_LEN] {
        &self.bytes
    }

    /// Generate a TFO cookie from a client IP address and a secret key.
    ///
    /// Uses a simplified HMAC-like hash: the IP bytes and the secret key
    /// are mixed through repeated XOR and rotate operations to produce
    /// an 8-byte cookie.  This is not cryptographically strong but
    /// serves as a placeholder for a real HMAC-SipHash implementation.
    pub fn generate(client_ip: &[u8; 4], secret: &[u8; 16]) -> Self {
        let mut state: u64 = 0;

        // Mix the secret key into the state.
        let k0 = u64::from_le_bytes([
            secret[0], secret[1], secret[2], secret[3], secret[4], secret[5], secret[6], secret[7],
        ]);
        let k1 = u64::from_le_bytes([
            secret[8], secret[9], secret[10], secret[11], secret[12], secret[13], secret[14],
            secret[15],
        ]);
        state = state.wrapping_add(k0);
        state ^= k1;

        // Mix the client IP into the state.
        let ip_val = u32::from_be_bytes(*client_ip) as u64;
        state = state.wrapping_add(ip_val);
        state ^= state.rotate_left(13);
        state = state.wrapping_mul(0x5bd1_e995_5bd1_e995);
        state ^= state.rotate_right(7);

        Self {
            bytes: state.to_le_bytes(),
        }
    }

    /// Validate this cookie against a freshly computed one.
    ///
    /// Returns `true` if the cookies match and the stored timestamp
    /// is within [`TFO_COOKIE_EXPIRY_TICKS`] of `current_tick`.
    pub fn validate(&self, expected: &TfoCookie, stored_tick: u64, current_tick: u64) -> bool {
        if self.bytes != expected.bytes {
            return false;
        }
        let age = current_tick.saturating_sub(stored_tick);
        age <= TFO_COOKIE_EXPIRY_TICKS
    }
}

// =========================================================================
// TfoCookieEntry
// =========================================================================

/// A single entry in the server-side cookie jar.
#[derive(Debug, Clone, Copy)]
struct TfoCookieEntry {
    /// Client IPv4 address this cookie was issued for.
    client_ip: [u8; 4],
    /// The cookie value.
    cookie: TfoCookie,
    /// Tick at which this cookie was generated.
    created_tick: u64,
    /// Whether this slot is occupied.
    in_use: bool,
}

impl TfoCookieEntry {
    /// An empty, unused entry.
    const EMPTY: Self = Self {
        client_ip: [0; 4],
        cookie: TfoCookie::new([0; TFO_COOKIE_LEN]),
        created_tick: 0,
        in_use: false,
    };
}

// =========================================================================
// TfoCookieJar
// =========================================================================

/// Server-side cookie storage for TCP Fast Open.
///
/// Maintains up to [`TFO_COOKIE_JAR_SIZE`] (64) cookie entries,
/// indexed by client IP address.  The server secret key can be
/// rotated at any time; existing cookies remain valid until they
/// expire.
pub struct TfoCookieJar {
    /// Cookie entries.
    entries: [TfoCookieEntry; TFO_COOKIE_JAR_SIZE],
    /// Current server secret key (16 bytes).
    secret: [u8; 16],
    /// Previous server secret key for rotation grace period.
    prev_secret: [u8; 16],
    /// Tick at which the current secret was installed.
    secret_rotation_tick: u64,
}

impl Default for TfoCookieJar {
    fn default() -> Self {
        Self::new([0u8; 16])
    }
}

impl TfoCookieJar {
    /// Create a new cookie jar with the given initial secret key.
    pub const fn new(secret: [u8; 16]) -> Self {
        Self {
            entries: [TfoCookieEntry::EMPTY; TFO_COOKIE_JAR_SIZE],
            secret,
            prev_secret: [0u8; 16],
            secret_rotation_tick: 0,
        }
    }

    /// Rotate the server secret key.
    ///
    /// The previous key is retained so that cookies generated just
    /// before rotation remain valid for the remainder of their
    /// expiry window.
    pub fn rotate_secret(&mut self, new_secret: [u8; 16], current_tick: u64) {
        self.prev_secret = self.secret;
        self.secret = new_secret;
        self.secret_rotation_tick = current_tick;
    }

    /// Generate and store a cookie for the given client IP.
    ///
    /// If a cookie for this IP already exists, it is replaced.
    /// If the jar is full, the oldest entry is evicted.
    pub fn generate(&mut self, client_ip: &[u8; 4], current_tick: u64) -> TfoCookie {
        let cookie = TfoCookie::generate(client_ip, &self.secret);

        // Look for an existing entry for this IP, or the oldest entry.
        let mut target_idx = 0;
        let mut oldest_tick = u64::MAX;
        let mut found_existing = false;

        for i in 0..TFO_COOKIE_JAR_SIZE {
            if !self.entries[i].in_use {
                target_idx = i;
                break;
            }
            if self.entries[i].client_ip == *client_ip {
                target_idx = i;
                found_existing = true;
                break;
            }
            if self.entries[i].created_tick < oldest_tick {
                oldest_tick = self.entries[i].created_tick;
                target_idx = i;
            }
        }

        let _ = found_existing; // Suppress unused warning; kept for clarity.

        self.entries[target_idx] = TfoCookieEntry {
            client_ip: *client_ip,
            cookie,
            created_tick: current_tick,
            in_use: true,
        };

        cookie
    }

    /// Validate a cookie presented by a client.
    ///
    /// Looks up the stored cookie for the client IP and checks
    /// that the presented cookie matches and has not expired.
    /// Also tries re-generating with the previous secret key to
    /// handle the rotation grace period.
    pub fn validate(&self, client_ip: &[u8; 4], presented: &TfoCookie, current_tick: u64) -> bool {
        // Look up the stored entry for this client IP.
        for i in 0..TFO_COOKIE_JAR_SIZE {
            if !self.entries[i].in_use {
                continue;
            }
            if self.entries[i].client_ip != *client_ip {
                continue;
            }

            // First, try matching against the stored cookie directly.
            if presented.validate(
                &self.entries[i].cookie,
                self.entries[i].created_tick,
                current_tick,
            ) {
                return true;
            }

            // Try previous secret (rotation grace period):
            // regenerate what the cookie would have been under the
            // old key and compare.
            let expected_prev = TfoCookie::generate(client_ip, &self.prev_secret);
            if presented.validate(&expected_prev, self.entries[i].created_tick, current_tick) {
                return true;
            }

            return false;
        }
        false
    }

    /// Remove expired entries from the jar.
    pub fn evict_expired(&mut self, current_tick: u64) {
        for i in 0..TFO_COOKIE_JAR_SIZE {
            if !self.entries[i].in_use {
                continue;
            }
            let age = current_tick.saturating_sub(self.entries[i].created_tick);
            if age > TFO_COOKIE_EXPIRY_TICKS {
                self.entries[i].in_use = false;
            }
        }
    }
}

// =========================================================================
// TfoStats
