/// A TCP connection extended with TCP Fast Open state.
///
/// Wraps the core TCP connection fields and adds TFO-specific
/// cookie handling and SYN+data support.  When TFO is enabled,
/// the client may include application data in the SYN packet,
/// and the server may deliver that data before the handshake
/// completes.
pub struct TfoConnection {
    /// TFO negotiation state.
    pub tfo_state: TfoState,
    /// Cookie for this connection (client-side cache or
    /// server-side issued cookie).
    pub cookie: TfoCookie,
    /// Local port number.
    pub local_port: u16,
    /// Remote port number.
    pub remote_port: u16,
    /// Local IPv4 address.
    pub local_ip: [u8; 4],
    /// Remote IPv4 address.
    pub remote_ip: [u8; 4],
    /// SND.NXT — next sequence number to send.
    pub send_next: u32,
    /// SND.UNA — oldest unacknowledged sequence number.
    pub send_unack: u32,
    /// RCV.NXT — next expected sequence number from the remote.
    pub recv_next: u32,
    /// Outgoing data buffer.
    send_buf: [u8; SEND_BUF_SIZE],
    /// Number of valid bytes in `send_buf`.
    send_len: usize,
    /// Incoming data buffer.
    recv_buf: [u8; RECV_BUF_SIZE],
    /// Number of valid bytes in `recv_buf`.
    recv_len: usize,
    /// Whether this connection slot is in use.
    in_use: bool,
}

impl Default for TfoConnection {
    fn default() -> Self {
        Self::new()
    }
}

impl TfoConnection {
    /// Create a new TFO connection in the disabled state.
    pub const fn new() -> Self {
        Self {
            tfo_state: TfoState::Disabled,
            cookie: TfoCookie::new([0; TFO_COOKIE_LEN]),
            local_port: 0,
            remote_port: 0,
            local_ip: [0; 4],
            remote_ip: [0; 4],
            send_next: 0,
            send_unack: 0,
            recv_next: 0,
            send_buf: [0; SEND_BUF_SIZE],
            send_len: 0,
            recv_buf: [0; RECV_BUF_SIZE],
            recv_len: 0,
            in_use: false,
        }
    }

    /// Buffer incoming data from a SYN+data segment.
    ///
    /// Copies up to [`RECV_BUF_SIZE`] bytes of SYN payload into the
    /// receive buffer.  Returns the number of bytes buffered.
    fn buffer_syn_data(&mut self, data: &[u8]) -> usize {
        let available = RECV_BUF_SIZE.saturating_sub(self.recv_len);
        let limit = if data.len() < TFO_MAX_SYN_DATA {
            data.len()
        } else {
            TFO_MAX_SYN_DATA
        };
        let count = if limit < available { limit } else { available };
        if count > 0 {
            self.recv_buf[self.recv_len..self.recv_len + count].copy_from_slice(&data[..count]);
            self.recv_len += count;
        }
        count
    }

    /// Read received data from the receive buffer.
    ///
    /// Copies up to `buf.len()` bytes and shifts the remaining data
    /// forward.  Returns the number of bytes actually read.
    pub fn recv(&mut self, buf: &mut [u8]) -> usize {
        let count = if buf.len() < self.recv_len {
            buf.len()
        } else {
            self.recv_len
        };
        if count > 0 {
            buf[..count].copy_from_slice(&self.recv_buf[..count]);
            let remaining = self.recv_len - count;
            if remaining > 0 {
                let mut i = 0;
                while i < remaining {
                    self.recv_buf[i] = self.recv_buf[count + i];
                    i += 1;
                }
            }
            self.recv_len = remaining;
        }
        count
    }

    /// Buffer outgoing data for transmission.
    ///
    /// Returns the number of bytes actually buffered.
    pub fn send(&mut self, data: &[u8]) -> usize {
        let available = SEND_BUF_SIZE.saturating_sub(self.send_len);
        let count = if data.len() < available {
            data.len()
        } else {
            available
        };
        if count > 0 {
            self.send_buf[self.send_len..self.send_len + count].copy_from_slice(&data[..count]);
            self.send_len += count;
        }
        count
    }

    /// Return the number of bytes available to read.
    pub const fn recv_available(&self) -> usize {
        self.recv_len
    }
}

