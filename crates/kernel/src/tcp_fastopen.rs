/// An entry in the TFO-enabled listening socket registry.
#[derive(Debug, Clone, Copy)]
struct TfoRegistryEntry {
    /// Listening port.
    port: u16,
    /// Whether TFO is enabled for this socket.
    enabled: bool,
    /// Whether this slot is in use.
    in_use: bool,
}

impl TfoRegistryEntry {
    /// An empty entry.
    const EMPTY: Self = Self {
        port: 0,
        enabled: false,
        in_use: false,
    };
}

// =========================================================================
// TfoRegistry
// =========================================================================

/// Registry of TFO-enabled listening sockets.
///
/// Tracks up to [`TFO_REGISTRY_SIZE`] (32) listening sockets that
/// have TFO enabled.  The kernel consults this registry when
/// processing incoming SYN packets that carry a TFO cookie.
pub struct TfoRegistry {
    /// Socket entries.
    entries: [TfoRegistryEntry; TFO_REGISTRY_SIZE],
    /// Global TFO statistics.
    pub stats: TfoStats,
    /// Server-side cookie jar.
    pub cookie_jar: TfoCookieJar,
}

impl Default for TfoRegistry {
    fn default() -> Self {
        Self::new([0u8; 16])
    }
}

impl TfoRegistry {
    /// Create a new registry with the given initial server secret.
    pub const fn new(secret: [u8; 16]) -> Self {
        Self {
            entries: [TfoRegistryEntry::EMPTY; TFO_REGISTRY_SIZE],
            stats: TfoStats::new(),
            cookie_jar: TfoCookieJar::new(secret),
        }
    }

    /// Enable TFO for a listening socket on the given port.
    ///
    /// # Errors
    ///
    /// Returns [`Error::AlreadyExists`] if TFO is already enabled
    /// on this port, or [`Error::OutOfMemory`] if the registry is
    /// full.
    pub fn enable(&mut self, port: u16) -> Result<()> {
        // Check for duplicates.
        for i in 0..TFO_REGISTRY_SIZE {
            if self.entries[i].in_use && self.entries[i].port == port {
                return Err(Error::AlreadyExists);
            }
        }
        // Find a free slot.
        for i in 0..TFO_REGISTRY_SIZE {
            if !self.entries[i].in_use {
                self.entries[i] = TfoRegistryEntry {
                    port,
                    enabled: true,
                    in_use: true,
                };
                return Ok(());
            }
        }
        Err(Error::OutOfMemory)
    }

    /// Disable TFO for a listening socket on the given port.
    ///
    /// # Errors
    ///
    /// Returns [`Error::NotFound`] if the port is not registered.
    pub fn disable(&mut self, port: u16) -> Result<()> {
        for i in 0..TFO_REGISTRY_SIZE {
            if self.entries[i].in_use && self.entries[i].port == port {
                self.entries[i].in_use = false;
                self.entries[i].enabled = false;
                return Ok(());
            }
        }
        Err(Error::NotFound)
    }

    /// Check whether TFO is enabled for the given port.
    pub fn is_enabled(&self, port: u16) -> bool {
        for i in 0..TFO_REGISTRY_SIZE {
            if self.entries[i].in_use && self.entries[i].port == port && self.entries[i].enabled {
                return true;
            }
        }
        false
    }
}

// =========================================================================
// Public API functions
// =========================================================================

/// Generate a TFO cookie for a client and record it in the jar.
///
/// Called by the server when a SYN is received with an empty TFO
/// cookie option (cookie request).  The generated cookie is
/// returned in the SYN+ACK.
///
/// # Errors
///
/// Returns [`Error::InvalidArgument`] if TFO is not enabled for
/// `server_port` in the given registry.
pub fn tfo_create_cookie(
    registry: &mut TfoRegistry,
    client_ip: &[u8; 4],
    server_port: u16,
    current_tick: u64,
) -> Result<TfoCookie> {
    if !registry.is_enabled(server_port) {
        return Err(Error::InvalidArgument);
    }
    registry.stats.requests_received += 1;
    let cookie = registry.cookie_jar.generate(client_ip, current_tick);
    registry.stats.cookies_generated += 1;
    Ok(cookie)
}

/// Validate a TFO cookie presented by a client.
///
/// Returns `true` if the cookie is valid and not expired.
/// Updates the registry statistics accordingly.
///
/// # Errors
///
/// Returns [`Error::InvalidArgument`] if TFO is not enabled for
/// `server_port`.
pub fn tfo_validate_cookie(
    registry: &mut TfoRegistry,
    client_ip: &[u8; 4],
    server_port: u16,
    presented: &TfoCookie,
    current_tick: u64,
) -> Result<bool> {
    if !registry.is_enabled(server_port) {
        return Err(Error::InvalidArgument);
    }
    registry.stats.requests_received += 1;

    let valid = registry
        .cookie_jar
        .validate(client_ip, presented, current_tick);
    if valid {
        registry.stats.cookies_valid += 1;
    } else {
        registry.stats.cookies_expired += 1;
    }
    Ok(valid)
}

/// Handle an incoming SYN that carries data (TFO fast path).
///
/// If the cookie is valid, the SYN data is buffered into the
/// connection's receive buffer and the connection transitions to
/// [`TfoState::Accepted`].  The caller should then complete the
/// handshake and deliver the data to the application.
///
/// # Errors
///
/// Returns [`Error::InvalidArgument`] if:
/// - TFO is not enabled for `server_port`.
/// - `syn_data` is empty (no data in SYN).
/// - The cookie validation fails.
pub fn tfo_handle_syn_data(
    registry: &mut TfoRegistry,
    conn: &mut TfoConnection,
    client_ip: &[u8; 4],
    server_port: u16,
    presented: &TfoCookie,
    syn_data: &[u8],
    current_tick: u64,
) -> Result<usize> {
    if syn_data.is_empty() {
        return Err(Error::InvalidArgument);
    }
    let valid = tfo_validate_cookie(registry, client_ip, server_port, presented, current_tick)?;
    if !valid {
        return Err(Error::InvalidArgument);
    }

    registry.stats.data_in_syn_sent += 1;

    conn.remote_ip = *client_ip;
    conn.local_port = server_port;
    conn.tfo_state = TfoState::Accepted;
    conn.in_use = true;

    let buffered = conn.buffer_syn_data(syn_data);
    conn.recv_next = conn.recv_next.wrapping_add(buffered as u32);

    Ok(buffered)
}

/// Initiate a TFO-enabled connection from the client side.
///
/// If the client has a cached cookie for the server, it may
/// include up to [`TFO_MAX_SYN_DATA`] bytes of application data
/// in the SYN.  The connection transitions to
/// [`TfoState::Requesting`] (no cookie) or
/// [`TfoState::Established`] (with cookie + data).
///
/// Returns the number of data bytes that will be included in the
/// SYN packet.
///
/// # Errors
///
/// Returns [`Error::InvalidArgument`] if the connection is already
/// in use.
pub fn tfo_connect_with_cookie(
    conn: &mut TfoConnection,
    server_ip: [u8; 4],
    server_port: u16,
    cookie: Option<&TfoCookie>,
    data: &[u8],
) -> Result<usize> {
    if conn.in_use {
        return Err(Error::InvalidArgument);
    }

    conn.remote_ip = server_ip;
    conn.remote_port = server_port;
    conn.in_use = true;

    match cookie {
        Some(c) => {
            conn.cookie = *c;
            let sent = conn.send(data);
            conn.tfo_state = TfoState::Established;
            Ok(sent)
        }
        None => {
            // No cookie — request one from the server.
            conn.tfo_state = TfoState::Requesting;
            Ok(0)
        }
    }
}
