/// Global TCP Fast Open statistics.
///
/// Tracks key metrics for monitoring TFO usage and diagnosing
/// cookie validation failures.
#[derive(Debug, Clone, Copy, Default)]
pub struct TfoStats {
    /// Number of SYN packets received with a TFO cookie option.
    pub requests_received: u64,
    /// Number of cookies successfully generated and issued.
    pub cookies_generated: u64,
    /// Number of presented cookies that passed validation.
    pub cookies_valid: u64,
    /// Number of presented cookies that were expired.
    pub cookies_expired: u64,
    /// Number of SYN packets that carried application data.
    pub data_in_syn_sent: u64,
}

impl TfoStats {
    /// Create a zeroed statistics block.
    pub const fn new() -> Self {
        Self {
            requests_received: 0,
            cookies_generated: 0,
            cookies_valid: 0,
            cookies_expired: 0,
            data_in_syn_sent: 0,
        }
    }

    /// Reset all counters to zero.
    pub fn reset(&mut self) {
        *self = Self::new();
    }
}

